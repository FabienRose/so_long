#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		totlen;
	char	*str;
	int		i;

	if (!s1 || !s2)
		return (NULL);
	totlen = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	str = malloc(sizeof(char) * (totlen + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (*s1)
	{
		str[i] = *s1;
		i++;
		s1++;
	}
	while (*s2)
	{
		str[i] = *s2;
		i++;
		s2++;
	}
	str[i] = '\0';
	return (str);
}
